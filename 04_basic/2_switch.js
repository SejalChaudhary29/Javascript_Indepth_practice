//switch case statement 

const month = 'May'
switch (month) {
    case 'Jan':
        console.log('January')
        
        break;
    case 'Feb':
        console.log('February')
        
        break;
    case 'Mar':
        console.log('March')
        
        break;
    case 'Apr':
        console.log('April')
        
        break;
    case 'May':
        console.log('May month matched')
        
        break;
    case 'Jun':
        console.log('June month matched')
        
        break;
    case 'July':
        console.log('July month matched')
        
      break;

    default:
        console.log('Not found')
        break;
}

// Note: if you didnt use break keyword. the matched value with other gets executed except the default execution .  


